# create a function first_five, that given a list, returns its first five elements

def first_five(a_list):
    return a_list[:5]

print(first_five(['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j']))


#create a function last_five, that given a list, returns its first five elements

def last_five(a_list):
    return a_list[-5:]

print(last_five(['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j']))